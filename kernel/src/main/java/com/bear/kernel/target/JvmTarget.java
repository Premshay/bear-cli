    @Override
    public void compile(BearIr ir, Path projectRoot, String blockKey) throws IOException {
        Files.createDirectories(projectRoot);

        String blockName = toPascalCase(ir.block().name());
        String effectiveBlockKey = blockKey == null ? "" : blockKey.trim();
        if (effectiveBlockKey.isEmpty()) {
            throw new IllegalArgumentException("blockKey must be non-empty");
        }
        String packageSegment = sanitizePackageSegment(ir.block().name());
        String packageName = "com.bear.generated." + packageSegment;
        String packagePath = packageName.replace('.', '/');
        String implPackageName = "blocks." + packageSegment + ".impl";
        String implPackagePath = implPackageName.replace('.', '/');

        Path generatedRoot = projectRoot.resolve("build").resolve("generated").resolve("bear");
        Path generatedMain = generatedRoot.resolve("src").resolve("main").resolve("java").resolve(packagePath);
        Path generatedTest = generatedRoot.resolve("src").resolve("test").resolve("java").resolve(packagePath);
        Path generatedCanonicalRuntimeMain = generatedRoot.resolve("src").resolve("main").resolve("java").resolve("com/bear/generated/runtime");
        Path generatedLegacyRuntimeRoot = generatedRoot.resolve("runtime");
        Path generatedSurfaces = generatedRoot.resolve("surfaces");
        Path generatedWiring = generatedRoot.resolve("wiring");
        Path surfaceMarker = generatedSurfaces.resolve(effectiveBlockKey + ".surface.json");
        Path wiringManifest = generatedWiring.resolve(effectiveBlockKey + ".wiring.json");
        Path userMain = projectRoot.resolve("src").resolve("main").resolve("java").resolve(implPackagePath);
        Path userMainLegacyByBlockKey = projectRoot
            .resolve("src")
            .resolve("main")
            .resolve("java")
            .resolve("blocks")
            .resolve(effectiveBlockKey)
            .resolve("impl");
        Path userMainLegacy = projectRoot.resolve("src").resolve("main").resolve("java").resolve(packagePath);
        Path generatedConfig = generatedRoot.resolve("config");
        Path generatedAllowedDeps = generatedConfig.resolve("allowed-deps");
        Path generatedGradle = generatedRoot.resolve("gradle");
        Path stagingRoot = generatedRoot.resolve(".staging").resolve(effectiveBlockKey + "-" + System.nanoTime());
        Path stagingMain = stagingRoot.resolve("src").resolve("main").resolve("java").resolve(packagePath);
        Path stagingTest = stagingRoot.resolve("src").resolve("test").resolve("java").resolve(packagePath);
        Path stagingSurfaces = stagingRoot.resolve("surfaces");
        Path stagingWiring = stagingRoot.resolve("wiring");
        Path stagingSurfaceMarker = stagingSurfaces.resolve(effectiveBlockKey + ".surface.json");
        Path stagingWiringManifest = stagingWiring.resolve(effectiveBlockKey + ".wiring.json");

        deleteDirectoryIfExists(stagingRoot);
        Files.createDirectories(stagingMain);
        Files.createDirectories(stagingTest);
        Files.createDirectories(stagingSurfaces);
        Files.createDirectories(stagingWiring);
        Files.createDirectories(userMain);

        List<PortModel> ports = mapPorts(ir.block().effects().allow());
        List<FieldModel> inputs = mapFields(ir.block().contract().inputs());
        List<FieldModel> outputs = mapFields(ir.block().contract().outputs());
        List<PortModel> logicPorts = logicPorts(ports, ir.block());

        String runtimeExceptionContent = renderInvariantException();
        writeIfDifferent(
            generatedCanonicalRuntimeMain.resolve("BearInvariantViolationException.java"),
            runtimeExceptionContent
        );
        deleteDirectoryIfExists(generatedLegacyRuntimeRoot);

        write(stagingMain.resolve("BearValue.java"), renderBearValue(packageName));
        write(stagingMain.resolve(blockName + "Request.java"), renderModel(packageName, blockName + "Request", inputs));
        write(stagingMain.resolve(blockName + "Result.java"), renderModel(packageName, blockName + "Result", outputs));
        write(stagingMain.resolve(blockName + "Logic.java"), renderLogic(packageName, blockName, logicPorts));
        write(
            stagingMain.resolve(blockName + ".java"),
            renderEntrypoint(packageName, blockName, effectiveBlockKey, implPackageName, ports, logicPorts, inputs, outputs, ir.block())
        );

        for (PortModel port : ports) {
            write(stagingMain.resolve(port.interfaceName + ".java"), renderPort(packageName, port));
        }

        if (ir.block().kind() == BearIr.BlockKind.LOGIC) {
            write(
                stagingTest.resolve(blockName + "StructuralDirectionTest.java"),
                renderStructuralDirectionTest(packageName, blockName, effectiveBlockKey, ports, logicPorts)
            );
            write(
                stagingTest.resolve(blockName + "StructuralReachTest.java"),
                renderStructuralReachTest(packageName, blockName, effectiveBlockKey, ports)
            );
        }

        write(stagingSurfaceMarker, renderSurfaceManifest(ir));
        write(
            stagingWiringManifest,
            renderWiringManifest(
                effectiveBlockKey,
                packageName,
                blockName,
                implPackageName,
                implPackagePath,
                ports,
                logicPorts,
                ir.block()
            )
        );

        try {
            syncDirectory(stagingMain, generatedMain);
            syncDirectory(stagingTest, generatedTest);
            syncFile(stagingSurfaceMarker, surfaceMarker);
            syncFile(stagingWiringManifest, wiringManifest);
            writeContainmentArtifacts(
                projectRoot,
                generatedAllowedDeps,
                generatedGradle,
                effectiveBlockKey,
                "src/main/java/" + implPackagePath,
                ir.block().impl()
            );
        } finally {
            try {
                deleteDirectoryIfExists(stagingRoot);
            } catch (IOException ignored) {
                // Best effort cleanup for staging content.
            }
        }

        Path userImpl = userMain.resolve(blockName + "Impl.java");
        Path legacyImpl = userMainLegacy.resolve(blockName + "Impl.java");
        Path legacyByBlockKeyImpl = userMainLegacyByBlockKey.resolve(blockName + "Impl.java");
        if (!Files.exists(userImpl) && !Files.exists(legacyImpl) && !Files.exists(legacyByBlockKeyImpl)) {
            write(userImpl, renderImplStub(implPackageName, packageName, blockName, logicPorts, outputs));
        }
    }

    @Override
    public void generateWiringOnly(BearIr ir, Path projectRoot, Path outputRoot, String blockKey) throws IOException {
        Files.createDirectories(projectRoot);
        Files.createDirectories(outputRoot);

        String effectiveBlockKey = blockKey == null ? "" : blockKey.trim();
        if (effectiveBlockKey.isEmpty()) {
            throw new IllegalArgumentException("blockKey must be non-empty");
        }

        String blockName = toPascalCase(ir.block().name());
        String packageSegment = sanitizePackageSegment(ir.block().name());
        String generatedPackageName = "com.bear.generated." + packageSegment;
        String implPackageName = "blocks." + packageSegment + ".impl";
        String implPackagePath = implPackageName.replace('.', '/');

        List<PortModel> ports = mapPorts(ir.block().effects().allow());
        List<PortModel> logicPorts = logicPorts(ports, ir.block());

        Path wiringDir = outputRoot.resolve("wiring");
        Path wiringManifest = wiringDir.resolve(effectiveBlockKey + ".wiring.json");
        writeIfDifferent(
            wiringManifest,
            renderWiringManifest(
                effectiveBlockKey,
                generatedPackageName,
                blockName,
                implPackageName,
                implPackagePath,
                ports,
                logicPorts,
                ir.block()
            )
        );
    }

    private String renderBearValue(String packageName) {
        return JvmRenderUnits.renderBearValue(GENERATED_HEADER, packageName);
    }

    private String renderInvariantException() {
        return JvmRenderUnits.renderInvariantException(RUNTIME_GENERATED_HEADER);
    }

    private String renderModel(String packageName, String className, List<FieldModel> fields) {
        StringBuilder s = new StringBuilder();
        s.append(GENERATED_HEADER).append("package ").append(packageName).append(";\n\n");
        if (usesBigDecimal(fields)) {
            s.append("import java.math.BigDecimal;\n\n");
        }
        s.append("public final class ").append(className).append(" {\n");
        for (FieldModel field : fields) {
            s.append("    private final ").append(field.javaType).append(" ").append(field.memberName).append(";\n");
        }
        s.append("\n    public ").append(className).append("(");
        for (int i = 0; i < fields.size(); i++) {
            if (i > 0) {
                s.append(", ");
            }
            s.append(fields.get(i).javaType).append(" ").append(fields.get(i).memberName);
        }
        s.append(") {\n");
        for (FieldModel field : fields) {
            s.append("        this.").append(field.memberName).append(" = ").append(field.memberName).append(";\n");
        }
        s.append("    }\n\n");
        for (FieldModel field : fields) {
            s.append("    public ").append(field.javaType).append(" ").append(field.getterName).append("() { return ")
                .append(field.memberName).append("; }\n");
        }
        s.append("}\n");
        return s.toString();
    }

    private String renderPort(String packageName, PortModel port) {
        StringBuilder s = new StringBuilder();
        s.append(GENERATED_HEADER).append("package ").append(packageName).append(";\n\n");
        s.append("public interface ").append(port.interfaceName).append(" {\n");
        for (String method : port.methods) {
            s.append("    BearValue ").append(method).append("(BearValue input);\n");
        }
        s.append("}\n");
        return s.toString();
    }

    private String renderLogic(String packageName, String blockName, List<PortModel> ports) {
        StringBuilder s = new StringBuilder();
        s.append(GENERATED_HEADER).append("package ").append(packageName).append(";\n\n");
        s.append("public interface ").append(blockName).append("Logic {\n");
        s.append("    ").append(blockName).append("Result execute(").append(blockName).append("Request request");
        for (PortModel port : ports) {
            s.append(", ").append(port.interfaceName).append(" ").append(port.variableName);
        }
        s.append(");\n}\n");
        return s.toString();
    }

    private String renderEntrypoint(
        String packageName,
        String blockName,
        String blockKey,
        String implPackageName,
        List<PortModel> ports,
        List<PortModel> logicPorts,
        List<FieldModel> inputs,
        List<FieldModel> outputs,
        BearIr.Block block
    ) {
        StringBuilder s = new StringBuilder();
        s.append(GENERATED_HEADER).append("package ").append(packageName).append(";\n\n");
        s.append("import java.math.BigDecimal;\n\n");
        s.append("import java.nio.charset.StandardCharsets;\n\n");
        s.append("import com.bear.generated.runtime.BearInvariantViolationException;\n\n");
        s.append("public final class ").append(blockName).append(" {\n");
        s.append("    private static final String BLOCK_KEY = ").append(javaString(blockKey)).append(";\n");
        for (PortModel port : ports) {
            s.append("    private final ").append(port.interfaceName).append(" ").append(port.variableName).append(";\n");
        }
        s.append("    private final ").append(blockName).append("Logic logic;\n\n");
        s.append("    public ").append(blockName).append("(");
        for (int i = 0; i < ports.size(); i++) {
            if (i > 0) {
                s.append(", ");
            }
            s.append(ports.get(i).interfaceName).append(" ").append(ports.get(i).variableName);
        }
        if (!ports.isEmpty()) {
            s.append(", ");
        }
        s.append(blockName).append("Logic logic) {\n");
        for (PortModel port : ports) {
            s.append("        this.").append(port.variableName).append(" = ").append(port.variableName).append(";\n");
        }
        s.append("        this.logic = logic;\n");
        s.append("    }\n\n");
        if (block.kind() == BearIr.BlockKind.LOGIC) {
            s.append("    public static ").append(blockName).append(" of(");
            for (int i = 0; i < ports.size(); i++) {
                if (i > 0) {
                    s.append(", ");
                }
                s.append(ports.get(i).interfaceName).append(" ").append(ports.get(i).variableName);
            }
            s.append(") {\n");
            s.append("        return new ").append(blockName).append("(");
            for (int i = 0; i < ports.size(); i++) {
                if (i > 0) {
                    s.append(", ");
                }
                s.append(ports.get(i).variableName);
            }
            if (!ports.isEmpty()) {
                s.append(", ");
            }
            s.append("new ").append(implPackageName).append(".").append(blockName).append("Impl());\n");
            s.append("    }\n\n");
        }
        s.append("    public ").append(blockName).append("Result execute(").append(blockName).append("Request request) {\n");

        if (block.idempotency() != null) {
            PortModel idPort = findPort(ports, block.idempotency().store().port());
            s.append("        String idempotencyKey = computeIdempotencyKey(request);\n");
            s.append("        BearValue existing = ").append(idPort.variableName).append(".")
                .append(toCamelCase(block.idempotency().store().getOp()))
                .append("(BearValue.builder().put(\"key\", idempotencyKey).build());\n");
            s.append("        if (existing != null && \"true\".equals(existing.get(\"hit\"))) {\n");
            s.append("            ").append(blockName).append("Result replayed = decodeResult(existing);\n");
            appendInvariantChecks(s, outputs, block, "replayed");
            s.append("            return replayed;\n");
            s.append("        }\n");
        }

        s.append("        ").append(blockName).append("Result result = logic.execute(request");
        for (PortModel port : logicPorts) {
            s.append(", ").append(port.variableName);
        }
        s.append(");\n");
        appendInvariantChecks(s, outputs, block, "result");

        if (block.idempotency() != null) {
            PortModel idPort = findPort(ports, block.idempotency().store().port());
            s.append("        BearValue.Builder stored = BearValue.builder().put(\"hit\", \"true\")")
                .append(".put(\"key\", idempotencyKey);\n");
            for (FieldModel output : outputs) {
                s.append("        stored.put(\"result.").append(output.originalName).append("\", toText(result.")
                    .append(output.getterName).append("()));\n");
            }
            s.append("        ").append(idPort.variableName).append(".").append(toCamelCase(block.idempotency().store().putOp()))
                .append("(stored.build());\n");
        }

        s.append("        return result;\n");
        s.append("    }\n\n");

        s.append("    private ").append(blockName).append("Result decodeResult(BearValue value) {\n");
        for (FieldModel output : outputs) {
            s.append("        if (value.get(\"result.")
                .append(output.originalName)
                .append("\") == null) {\n");
            s.append("            throw new IllegalStateException(\"idempotency replay payload missing field: result.")
                .append(output.originalName)
                .append("\");\n");
            s.append("        }\n");
        }
        s.append("        return new ").append(blockName).append("Result(");
        for (int i = 0; i < outputs.size(); i++) {
            if (i > 0) {
                s.append(", ");
            }
            FieldModel output = outputs.get(i);
            s.append("fromText(value.get(\"result.")
                .append(output.originalName)
                .append("\"), ")
                .append(output.javaType)
                .append(".class, \"result.")
                .append(output.originalName)
                .append("\")");
        }
        s.append(");\n");
        s.append("    }\n\n");

        if (block.idempotency() != null) {
            List<FieldModel> keyFields = idempotencyKeyFields(inputs, block.idempotency());
            s.append("    private String computeIdempotencyKey(").append(blockName).append("Request request) {\n");
            s.append("        StringBuilder key = new StringBuilder(BLOCK_KEY);\n");
            for (FieldModel keyField : keyFields) {
                s.append("        String raw_").append(keyField.memberName).append(" = toText(request.")
                    .append(keyField.getterName).append("());\n");
                s.append("        if (raw_").append(keyField.memberName).append(" == null) {\n");
                s.append("            throw new IllegalArgumentException(\"idempotency key field is null: ")
                    .append(keyField.originalName).append("\");\n");
                s.append("        }\n");
                s.append("        String escaped_").append(keyField.memberName).append(" = escapeDelimitedValue(raw_")
                    .append(keyField.memberName).append(");\n");
                s.append("        key.append(\"|\")")
                    .append(".append(escapeDelimitedValue(").append(javaString(keyField.originalName)).append("))")
                    .append(".append(\"=\")")
                    .append(".append(escaped_").append(keyField.memberName).append(".getBytes(StandardCharsets.UTF_8).length)")
                    .append(".append(\"#\")")
                    .append(".append(escaped_").append(keyField.memberName).append(");\n");
            }
            s.append("        return key.toString();\n");
            s.append("    }\n\n");
        }

        s.append("    private static String toText(Object value) {\n");
        s.append("        if (value == null) return null;\n");
        s.append("        if (value instanceof BigDecimal decimal) return decimal.toString();\n");
        s.append("        if (value instanceof Boolean bool) return bool ? \"true\" : \"false\";\n");
        s.append("        if (value instanceof Integer integer) return Integer.toString(integer);\n");
        s.append("        return String.valueOf(value);\n");
        s.append("    }\n");
        s.append("    @SuppressWarnings(\"unchecked\")\n");
        s.append("    private static <T> T fromText(String text, Class<T> type, String fieldName) {\n");
        s.append("        if (text == null) return null;\n");
        s.append("        try {\n");
        s.append("            if (type == String.class) return (T) text;\n");
        s.append("            if (type == BigDecimal.class) return (T) new BigDecimal(text);\n");
        s.append("            if (type == Integer.class) return (T) Integer.valueOf(text);\n");
        s.append("            if (type == Boolean.class) {\n");
        s.append("                if (\"true\".equals(text)) return (T) Boolean.TRUE;\n");
        s.append("                if (\"false\".equals(text)) return (T) Boolean.FALSE;\n");
        s.append("                throw new IllegalArgumentException(\"invalid boolean value\");\n");
        s.append("            }\n");
        s.append("        } catch (RuntimeException e) {\n");
        s.append("            throw new IllegalStateException(\"idempotency replay payload invalid value: \" + fieldName, e);\n");
        s.append("        }\n");
        s.append("        throw new IllegalArgumentException(\"Unsupported type: \" + type.getName());\n");
        s.append("    }\n");
        s.append("    private static String toInvariantCanonical(Object value) {\n");
        s.append("        if (value == null) return \"<null>\";\n");
        s.append("        if (value instanceof BigDecimal decimal) return decimal.toString();\n");
        s.append("        if (value instanceof Boolean bool) return bool ? \"true\" : \"false\";\n");
        s.append("        if (value instanceof Integer integer) return Integer.toString(integer);\n");
        s.append("        return String.valueOf(value);\n");
        s.append("    }\n");
        s.append("    private static String escapeDelimitedValue(String value) {\n");
        s.append("        if (value == null) return \"\";\n");
        s.append("        return value.replace(\"\\\\\", \"\\\\\\\\\")\n");
        s.append("            .replace(\"|\", \"\\\\|\")\n");
        s.append("            .replace(\"=\", \"\\\\=\")\n");
        s.append("            .replace(\"#\", \"\\\\#\")\n");
        s.append("            .replace(\"\\n\", \"\\\\n\")\n");
        s.append("            .replace(\"\\r\", \"\\\\r\");\n");
        s.append("    }\n");
        s.append("    private static String invariantMarker(String kind, String field, String observed, String rule) {\n");
        s.append("        return \"BEAR_INVARIANT_VIOLATION|\"\n");
        s.append("            + \"block=\" + escapeDelimitedValue(BLOCK_KEY)\n");
        s.append("            + \"|kind=\" + escapeDelimitedValue(kind)\n");
        s.append("            + \"|field=\" + escapeDelimitedValue(field)\n");
        s.append("            + \"|observed=\" + escapeDelimitedValue(observed)\n");
        s.append("            + \"|rule=\" + escapeDelimitedValue(rule);\n");
        s.append("    }\n");
        s.append("}\n");
        return s.toString();
    }

    private void appendInvariantChecks(
        StringBuilder s,
        List<FieldModel> outputs,
        BearIr.Block block,
        String resultRef
    ) {
        if (block.invariants() == null) {
            return;
        }
        for (BearIr.Invariant invariant : block.invariants()) {
            FieldModel output = findField(outputs, invariant.field());
            String getterCall = resultRef + "." + output.getterName + "()";
            String ruleLiteral = javaString(invariantRuleText(invariant));
            switch (invariant.kind()) {
                case NON_NEGATIVE -> {
                    if ("BigDecimal".equals(output.javaType)) {
                        s.append("        if (").append(getterCall).append(" == null || ")
                            .append(getterCall).append(".compareTo(BigDecimal.ZERO) < 0) {\n");
                    } else {
                        s.append("        if (").append(getterCall).append(" == null || ")
                            .append(getterCall).append(" < 0) {\n");
                    }
                    s.append("            throw new BearInvariantViolationException(invariantMarker(\"non_negative\", ")
                        .append(javaString(invariant.field()))
                        .append(", toInvariantCanonical(").append(getterCall).append("), ")
                        .append(ruleLiteral).append("));\n");
                    s.append("        }\n");
                }
                case NON_EMPTY -> {
                    s.append("        if (").append(getterCall).append(" == null || ").append(getterCall).append(".isEmpty()) {\n");
                    s.append("            throw new BearInvariantViolationException(invariantMarker(\"non_empty\", ")
                        .append(javaString(invariant.field()))
                        .append(", toInvariantCanonical(").append(getterCall).append("), ")
                        .append(ruleLiteral).append("));\n");
                    s.append("        }\n");
                }
                case EQUALS -> {
                    s.append("        if (!toInvariantCanonical(").append(getterCall).append(").equals(")
                        .append(javaString(invariant.params().value())).append(")) {\n");
                    s.append("            throw new BearInvariantViolationException(invariantMarker(\"equals\", ")
                        .append(javaString(invariant.field()))
                        .append(", toInvariantCanonical(").append(getterCall).append("), ")
                        .append(ruleLiteral).append("));\n");
                    s.append("        }\n");
                }
                case ONE_OF -> {
                    s.append("        if (!").append(oneOfSetLiteral(invariant.params().values())).append(".contains(toInvariantCanonical(")
                        .append(getterCall).append("))) {\n");
                    s.append("            throw new BearInvariantViolationException(invariantMarker(\"one_of\", ")
                        .append(javaString(invariant.field()))
                        .append(", toInvariantCanonical(").append(getterCall).append("), ")
                        .append(ruleLiteral).append("));\n");
                    s.append("        }\n");
                }
            }
        }
    }

    private String renderImplStub(
        String implPackageName,
        String generatedPackageName,
        String blockName,
        List<PortModel> ports,
        List<FieldModel> outputs
    ) {
        List<String> imports = new ArrayList<>();
        imports.add(generatedPackageName + "." + blockName + "Logic");
        imports.add(generatedPackageName + "." + blockName + "Request");
        imports.add(generatedPackageName + "." + blockName + "Result");
        for (PortModel port : ports) {
            imports.add(generatedPackageName + "." + port.interfaceName);
        }
        imports.sort(String::compareTo);

        StringBuilder s = new StringBuilder();
        s.append("package ").append(implPackageName).append(";\n\n");
        for (String importType : imports) {
            s.append("import ").append(importType).append(";\n");
        }
        if (usesBigDecimal(outputs)) {
            s.append("import java.math.BigDecimal;\n");
        }
        if (!imports.isEmpty() || usesBigDecimal(outputs)) {
            s.append("\n");
        }
        s.append("public final class ").append(blockName).append("Impl implements ").append(blockName).append("Logic {\n");
        s.append("    @Override\n");
        s.append("    public ").append(blockName).append("Result execute(").append(blockName).append("Request request");
        for (PortModel port : ports) {
            s.append(", ").append(port.interfaceName).append(" ").append(port.variableName);
        }
        s.append(") {\n");
        s.append("        // TODO: replace this entire method body with business logic.\n");
        s.append("        // Do not append logic below this placeholder return.\n");
        for (PortModel port : ports) {
            s.append("        // BEAR:PORT_USED ").append(port.variableName).append("\n");
        }
        s.append("        return new ").append(blockName).append("Result(");
        for (int i = 0; i < outputs.size(); i++) {
            if (i > 0) {
                s.append(", ");
            }
            s.append(defaultLiteral(outputs.get(i).javaType));
        }
        s.append(");\n");
        s.append("    }\n}\n");
        return s.toString();
    }

